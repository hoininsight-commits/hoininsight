export async function initVideoView(container) {
    container.innerHTML = `
        <div class="p-8 flex flex-col items-center justify-center space-y-4">
            <div class="w-8 h-8 border-2 border-blue-500 border-t-transparent rounded-full animate-spin"></div>
            <div class="text-[10px] font-black text-slate-500 uppercase tracking-[0.2em]">Loading Video Intelligence...</div>
        </div>
    `;

    try {
        const poolPaths = ['data/ops/video_candidate_pool.json'];
        const scriptPaths = ['data/ops/video_script_pack.json'];
        const linkagePaths = ['data/ops/stock_linkage_pack.json'];
        const densityPaths = ['data/ops/conflict_density_pack.json'];

        let poolData = null;
        for (const path of poolPaths) {
            try {
                const res = await fetch(`${path}?v=${Date.now()}`);
                if (res.ok) { poolData = await res.json(); break; }
            } catch (e) { }
        }

        let scriptData = null;
        for (const path of scriptPaths) {
            try {
                const res = await fetch(path);
                if (res.ok) { scriptData = await res.json(); break; }
            } catch (e) { }
        }

        let linkageData = null;
        for (const path of linkagePaths) {
            try {
                const res = await fetch(path);
                if (res.ok) { linkageData = await res.json(); break; }
            } catch (e) { }
        }

        let densityData = null;
        for (const path of densityPaths) {
            try {
                const res = await fetch(path);
                if (res.ok) { densityData = await res.json(); break; }
            } catch (e) { }
        }

        if (!poolData || !poolData.top_candidates || poolData.top_candidates.length === 0) {
            renderEmptyVideoState(container);
            return;
        }

        const mergedCandidates = poolData.top_candidates.map(c => {
            const sMatch = scriptData?.candidates?.find(sc => sc.dataset_id === c.dataset_id);
            const lMatch = linkageData?.topics?.find(lt => lt.dataset_id === c.dataset_id);
            const dMatch = densityData?.topics?.find(dt => dt.dataset_id === c.dataset_id);
            return {
                ...c,
                script: sMatch ? sMatch.script : null,
                linkage: lMatch || null,
                density: dMatch || null
            };
        });

        renderVideoCandidates(container, mergedCandidates);
    } catch (err) {
        console.error('[VideoView] Error:', err);
        container.innerHTML = `<div class="p-8 text-red-500 font-mono text-xs bg-red-500/5 rounded border border-red-500/20 m-4">[VIDEO_ERROR] Load Failed: ${err.message}</div>`;
    }
}

function renderEmptyVideoState(container) {
    container.innerHTML = `
        <div class="max-w-4xl mx-auto py-20 px-4 text-center">
            <div class="text-6xl mb-6 opacity-20">🎬</div>
            <h3 class="text-xl font-black text-slate-400 mb-2 uppercase tracking-tighter">현재 선별된 영상 후보가 없습니다</h3>
            <p class="text-sm text-slate-500 max-w-md mx-auto leading-relaxed">NS 점수 및 구조적 조건(Axis, Trigger)을 충족하는 지능형 선별 토픽이 발견되면 이곳에 자동으로 표시됩니다.</p>
        </div>
    `;
}

function renderVideoCandidates(container, candidates) {
    candidates.sort((a, b) => (b.video_score || 0) - (a.video_score || 0));
    const top3 = candidates.slice(0, 3);

    container.innerHTML = `
        <div class="space-y-8 fade-in pb-20">
            <header class="mb-10 px-4">
                <div class="text-[10px] font-black text-blue-500 uppercase tracking-[0.4em] mb-2">Video Intelligence Layer v1.0</div>
                <h1 class="text-4xl font-black text-white tracking-tighter uppercase leading-none">편집 회의 보드</h1>
                <p class="text-slate-500 text-sm mt-4 font-medium max-w-2xl">상위 3개 토픽과 자동 생성된 **영상 스크립트 패키지** 및 **구조적 노출 종목**입니다.</p>
            </header>
            <div class="grid gap-8 px-4">${top3.map((c, idx) => renderVideoCard(c, idx)).join('')}</div>
            <footer class="mt-20 py-10 border-t border-slate-800 text-center opacity-30 px-4"><div class="text-[10px] font-black text-slate-500 uppercase tracking-widest">Generated by HOIN Script Intelligence Engine v1.0</div></footer>
        </div>
    `;
}

